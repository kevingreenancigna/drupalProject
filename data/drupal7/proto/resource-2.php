             <h1>Where to start?</h1>
            <p>Making the decision to move abroad isn’t something to be taken lightly. There’s plenty of planning to of boxes to be ticked, forms to be filled out, and general planning to be done. But where do you start? Unfortunately there’s a little more to it than just packing your bags and jumping on a plane. Here are some of the things you’ll need to factor in before you head for pastures new…</p>
            <h4>Paperwork</h4>
            <p>First up, visas. Assuming you have a job waiting for you on arrival, then be sure to speak to your employer at your earliest possible convenience about visa support. Some businesses will take care of everything for you, including the costs; but others will leave it up to you. Find out what and how much you need to pay, then budget!<br>
              Depending on where you&rsquo;re headed for, visas and work permits can be a complicated affair. There could be a lot of paperwork, and some countries require you to have a full medical examination (at a cost to you). This can be arranged through your local doctor.</p>
            <p>Other factors include a potential Police Clearance Certificate. This can be applicable when travelling to the likes of Australia, Belgium, Canada, New Zealand, South Africa, and the USA. The cost can vary, generally between £45-80 (€55-97).<br>
              It&rsquo;s always worth making copies of your original documents too, as embassies have been known to be less than forthcoming when it comes to returning your documents. </p>
            <h4>Healthcare</h4>
            <p id="Again_this_really_comes_down_to_where_you_are_going_Some_coun">Again, this really comes down to where you are going. Some countries provide free state healthcare; however, some do not.<br>
              Some companies provide private medical insurance as part of their employee benefit package options. If you are in any doubt, contact your employer and find out the exact details of any cover they are providing. It is vitally important that you have comprehensive health insurance for you and your family. We at Cigna have a wide range of levels of expat medical insurance cover available to protect you in your new homeland, and anywhere else you may be travelling*. Find out more about Cigna Global Health Options insurance here.</p>
            <p>Be sure to check the health advice recommendations (including vaccinations etc) for your new country of residence. A handy guide to some of the more popular destinations can be found here.</p>
            <p>*certain geographical exclusions may apply depending on level of cover.</p>
            <h4>Travel</h4>
            <p>It goes without saying that air travel can be expensive, but it is of course a necessary expense if you are to become an expat. That said, there are some ways to reduce the cost of air travel. Booking a good while in advance generally results in discounts for long haul flights. Be sure to check out baggage allowances for the airlines your considering flying with, as some heavily restrict your weight limit, while others allow you to carry sports equipment for free. Travel insurance is also a must. As well as cost, make sure you&rsquo;re happy with the whole package of your travel insurance, including things like cancellation cover and baggage cover.</p>
            <h4>Shipping or Storage?</h4>
            <p>Just like everyone else, you too will have accumulated much more in the way of possessions than you previously thought. So what do you take with you?</p>
            <p> If you’re planning on renting at first in your new homeland, you could consider renting a fully furnished property and put your furniture into storage until you’re settled. You could then arrange to have your belongings shipped over at a more convenient time.</p>
